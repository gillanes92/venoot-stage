          <h2>From linear to circular</h2>
          <p>The world economy is experiencing a turning point. Its production processes must adapt to continue to exist and the end of the linear era is approaching: extract resources - transform them - produce - consume - dispose. Environmental and social factors are pushing the current model to transform  into a circle: with efficient processes that use NCRE and recycled materials, that manage their waste, that reduce and reuse.
Multiple business opportunities for companies of all sizes, although SMEs play a fundamental role due to their flexibility, they open up in this new scenario, through collaborative work, synergies and transfer of good practices.

Eurochile invites companies, NGOs, institutions, authorities and social actors to this conference, to learn about the advanced path in Circular Economy and how much remains to be done, with European and national experts; in addition to opening a space for connection between national and European companies that are already part of this ecosystem or that want to be.
